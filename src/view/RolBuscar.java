    private void btn_buscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_buscarActionPerformed
        // TODO add your handling code here:
       // TODO add your handling code here:
         if (tf_valor.getText().length()==0){
            JOptionPane.showMessageDialog(null,"Ingrese algún valor para efectuar la búsqueda", "Advertencia",JOptionPane.ERROR_MESSAGE);
            return;
        }
        else{
         if (list_filtros.getSelectedItem()=="Identificacion"){
            id=Integer.parseInt(tf_valor.getText());
            query = entityManager.createNamedQuery( "Rol.findByIdRol");
            query.setParameter("idRol",id);
            List<Rol> r = query.getResultList();
            if (r.size()==0){
                JOptionPane.showMessageDialog(null,"Identificación inexistente", "Error",JOptionPane.ERROR_MESSAGE);
                 tf_valor.setText(null);
                 return;
            }
            list.clear();
            list.addAll(r);
        }
        else{
             query = entityManager.createNativeQuery( "SELECT * FROM rol WHERE nombre LIKE "
                    +"'%"+tf_valor.getText()+"%'", Rol.class);
            List<Rol> r = query.getResultList();
             if (r.isEmpty()){
                 JOptionPane.showMessageDialog(null, "Nombre inexistente","Error",JOptionPane.ERROR_MESSAGE );
                 tf_valor.setText(null);
                  return;
            }
            list.clear();
            list.addAll(r);

        }      
    }
      

    }//GEN-LAST:event_btn_buscarActionPerformed

    private void masterTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_masterTableMouseClicked
        // TODO add your handling code here:
      switch (MenuAdminSist.opcion){
          case 1:
                cantidad=masterTable.getSelectedRow();
                id=(Integer) masterTable.getValueAt(cantidad, 0);
                nombre=(String)masterTable.getValueAt(cantidad, 1);
                 iden=Integer.toString(id);
                 JFrame frame=new RolEdit();
                 RolEdit.tf_identi.setText(iden);
                 RolEdit.tf_nombre.setText(nombre);
                 frame.setVisible(true);
                 frame.setTitle("Editar Rol");
                 frame.setLocationRelativeTo(null);
                 frame.setDefaultCloseOperation(JFrame.DISPOSE_ON_CLOSE);
                 this.setVisible(false);
                 break;
          case 2:
               cantidad=masterTable.getSelectedRow();
                id=(Integer) masterTable.getValueAt(cantidad, 0);
                nombre=(String)masterTable.getValueAt(cantidad, 1);
                 iden=Integer.toString(id);
                 JFrame frame2=new RolEliminar();
                 RolEliminar.tf_identi.setText(iden);
                 RolEliminar.tf_nombre.setText(nombre);
                 frame2.setVisible(true);
                 frame2.setTitle("Eliminar Rol");
                 frame2.setLocationRelativeTo(null);
                 frame2.setDefaultCloseOperation(JFrame.DISPOSE_ON_CLOSE);
                 this.setVisible(false);
                 break;

          case 3:
              break;
      }
    }//GEN-LAST:event_masterTableMouseClicked

