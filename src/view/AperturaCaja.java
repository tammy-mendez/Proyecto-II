    private void btn_guardar3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_guardar3ActionPerformed
          
        if(tf_importeCaja.getText().length()==0){
            JOptionPane.showMessageDialog(null,"Debe ingresar el monto en caja chica", "Error",JOptionPane.ERROR_MESSAGE);
            return;
            }
       
                query = entityManager.createNamedQuery("Caja.findByEstado");
                query.setParameter("estado",'H');
                List<Caja> cj = query.getResultList();
                if (!cj.isEmpty()){
                    JOptionPane.showMessageDialog(null, "La caja se encuentra Habilitada, no puede habilitar otra","Error",JOptionPane.ERROR_MESSAGE );
                    tf_fecha.setText(null);
                    tf_importeCaja.setText(null);
                    return;
                }
              
    else  {
              Caja c= new Caja();
              c.setFechaHora(tf_fecha.getText());
              c.setMontoInicial(Integer.parseInt(tf_importeCaja.getText()));
              c.setMontoFinal(0);
              c.setEstado('H');
              entityManager.getTransaction().begin();
              entityManager.persist(c);
              entityManager.flush();
               //registramos los datos de la auditoria
                    AuditoriaSistema as=new AuditoriaSistema();
                    as.setAccion("Registrar");
                    as.setTabla("Apertura Caja");
                    as.setAntes(c.toString());
                    as.setDespues("No hay cambios");
                    //trabajamos con la fecha
                    Date fecha=new Date();
                    DateFormat formato=new SimpleDateFormat("dd-MM-yyyy HH:mm:ss");
                    as.setFechaHora((formato.format(fecha)));
                    as.setUsuario(LoginView.nombreUsuario);
                    entityManager.persist(as);
                    entityManager.getTransaction().commit();
                    entityManager.close();
                    JOptionPane.showMessageDialog(null,"Apertura de Caja Exitosa", "Confirmaci√≥n",JOptionPane.INFORMATION_MESSAGE);
               
                  
          }  
         this.dispose(); 
         
        
    }//GEN-LAST:event_btn_guardar3ActionPerformed

