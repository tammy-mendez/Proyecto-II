    private void btn_eliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_eliminarActionPerformed
        // TODO add your handling code here:
        String valor;
        
        query= entityManager.createNativeQuery("select * from categoria_articulo ca"+
                " join articulo a"+
                " on a.cod_categoria=ca.cod_categoria"+
                " where ca.cod_categoria="+Integer.valueOf(tf_codigoCategoria.getText()), CategoriaArticulo.class);
         List<CategoriaArticulo> c=query.getResultList();
         if (!c.isEmpty()){
             JOptionPane.showMessageDialog(null, "Esta categoria posee movimientos, no puede eliminar","Error",JOptionPane.ERROR_MESSAGE );
            vaciar();
         }
         else{
        resp=  JOptionPane.showConfirmDialog(null," Esta seguro que desea eliminar?", "Confirmar Eliminación",JOptionPane.YES_NO_OPTION );
            if(resp==JOptionPane.YES_OPTION){
            
            entityManager.getTransaction().begin();
            CategoriaArticulo cat=entityManager.find(CategoriaArticulo.class,Integer.parseInt(tf_codigoCategoria.getText()) );
            valor=cat.toString();//guardamos el objeto antes de eliminar
            entityManager.remove(cat);
            
            //registramos los datos necesarios para la auditoria
           registrarAuditoria("Categoria Articulo","Eliminar",valor, " ");
            
            entityManager.getTransaction().commit();
            //entityManager.close();
            JOptionPane.showMessageDialog(null, "Eliminación Exitosa");
            vaciar();
            refrescar();
            
        }}
        
    }//GEN-LAST:event_btn_eliminarActionPerformed

