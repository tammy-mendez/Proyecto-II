    private void btn_guardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_guardarActionPerformed
        // TODO add your handling code here:
        String antes;
        String despues;
        int codigo;
        
            resp=  JOptionPane.showConfirmDialog(null,"Desea Modificar una nueva Categoría de Articulo?", "Confirmar Creación",JOptionPane.YES_NO_OPTION );
            if (resp==JOptionPane.YES_OPTION){
                query=entityManager.createNamedQuery("CategoriaArticulo.findByCodCategoria");
                query.setParameter("codCategoria",Integer.parseInt(tf_codigoCategoria.getText()));
                List<CategoriaArticulo> c=query.getResultList();
                //antes de los cambios
                antes=c.get(0).toString();
                CategoriaArticulo ca= new CategoriaArticulo();
                ca.setCodCategoria(Integer.parseInt(tf_codigoCategoria.getText()));
                ca.setDescripcion(tf_nombre.getText().toLowerCase());
                entityManager.getTransaction().begin();
                entityManager.merge(ca);
                entityManager.flush();
                //despues de los cambios
                despues=ca.toString();
                //registramos los datos necesarios para la auditoria
                entityManager.persist(registrarAuditoria("Modificar","Categoria Articulo", antes, despues));
                entityManager.getTransaction().commit();
               // entityManager.close();
                
                JOptionPane.showMessageDialog(null, "Modificación Exitosa");
                vaciar();
                refrescar();
            }
      btn_guardar.setEnabled(false);
    }//GEN-LAST:event_btn_guardarActionPerformed

