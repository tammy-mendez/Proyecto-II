    private void btn_eliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_eliminarActionPerformed
        // TODO add your handling code here:
        String valor;
        resp=  JOptionPane.showConfirmDialog(null,"Esta seguro que desea eliminar?", "Confirmar Eliminaci贸n",JOptionPane.YES_NO_OPTION );
        if(resp==JOptionPane.YES_OPTION){
            EntityManagerFactory fact=Persistence.createEntityManagerFactory("proyectoPU");
            EntityManager ema= fact.createEntityManager();
            ema.getTransaction().begin();
            Rol rolFind=ema.find(Rol.class,Integer.parseInt(tf_identi.getText()) );
            valor=rolFind.toString();//guardamos el objeto antes de eliminar
            ema.remove(rolFind);
            AuditoriaSistema as=new AuditoriaSistema();
            as.setAccion("Eliminaci贸n");
            as.setTabla("Rol");
            Date fecha=new Date();
            DateFormat formato=new SimpleDateFormat("dd-MM-yyyy HH:mm:ss");
            as.setFechaHora(formato.format(fecha));
            as.setUsuario(LoginView.nombreUsuario);
            as.setAntes(valor);
            as.setDespues("No hay modificaciones");
            ema.persist(as);
            ema.getTransaction().commit();
            ema.close();
            JOptionPane.showMessageDialog(null, "Eliminaci贸n Exitosa");
        }
        this.dispose();
    }//GEN-LAST:event_btn_eliminarActionPerformed

