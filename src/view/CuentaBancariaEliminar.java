    private void btn_guardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_guardarActionPerformed
        // TODO add your handling code here:
        if(obtenerBanco(tf_entidad.getText()) == null){
            JOptionPane.showMessageDialog(null,"Entidad Financiera Inexistente", "Advertencia",JOptionPane.ERROR_MESSAGE);
            return;
        }
        else if (tf_numeroCuenta.getText().length()==0
                && tf_entidad.getText().length()==0){
            JOptionPane.showMessageDialog(null,"Ingrese algun valor para "
                    + "los campos obligatorios", "Advertencia",JOptionPane.ERROR_MESSAGE);
            return;
        }else{
            EntityManagerFactory fact = Persistence.createEntityManagerFactory("proyectoPU");
            EntityManager entityManager = fact.createEntityManager();
            //Query query=entityManager.createNamedQuery("Rol.findByNombre");
            //query.setParameter("nombre",tf_nombre.getText().toLowerCase());
            //List<Rol> rol= query.getResultList();
            /*if(rol.size()>=1){//comprueba si ya existe un rol con el mismo nombre
                JOptionPane.showMessageDialog(null,"Ya existe un rol con el mismo nombre", "Aviso",JOptionPane.ERROR_MESSAGE);
                tf_nombre.setText(null);
                return;
            }*/
            //else{
                int resp=  JOptionPane.showConfirmDialog(null,"Desea eliminar la cuenta?", "Confirmar Creación",JOptionPane.YES_NO_OPTION );
                if (resp==JOptionPane.YES_OPTION){
                    entityManager.getTransaction().begin();
                    String antes = cuentaGlobal.toString();
                    CuentaBancaria cuentaFind = entityManager.find(CuentaBancaria.class, 
                            cuentaGlobal.getIdCuentaBancaria());
                    entityManager.remove(cuentaFind);
                    entityManager.flush();
                    //es similar a un commit
                    //registramos los datos necesarios para la auditoria
                    AuditoriaSistema as=new AuditoriaSistema();
                    as.setAccion("Eliminación");
                    as.setTabla("Cuenta Bancaria");
                    as.setAntes(antes);
                    as.setDespues("Sin cambios");
                    //trabajamos con la fecha
                    Date fecha=new Date();
                    DateFormat formato=new SimpleDateFormat("dd-MM-yyyy HH:mm:ss");
                    as.setFechaHora(formato.format(fecha));
                    as.setUsuario("vladimir");
                    //as.setUsuario(LoginView.nombreUsuario);
                    try{
                        entityManager.persist(as);
                        entityManager.getTransaction().commit();
                        JOptionPane.showMessageDialog(null,"Eliminación exitosa",
                            "Confirmación",JOptionPane.INFORMATION_MESSAGE);
                    }catch(javax.persistence.PersistenceException e){
                        JOptionPane.showMessageDialog(null, "La cuenta ya fue registrado");
                        tf_numeroCuenta.setText("");
                    }

                    entityManager.close();

                    //  }
            }
        }
        //vaciarCampos();
        this.dispose();
    }//GEN-LAST:event_btn_guardarActionPerformed

