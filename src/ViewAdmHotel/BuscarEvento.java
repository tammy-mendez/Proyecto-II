 private Eventos obtenerEvento(int fila){
        query=entityManager.createNamedQuery("Eventos.findByIdEvento");
        query.setParameter("idEvento", masterTable.getValueAt(fila, 0));
        Eventos ev=(Eventos) query.getSingleResult();
        return ev;
    }
