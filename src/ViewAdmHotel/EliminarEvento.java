    private void btn_eliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_eliminarActionPerformed
        // TODO add your handling code here:
        int cod;
        String valor;
        if(tf_codEvento.getText().length()==0){
            JOptionPane.showMessageDialog(null,"Seleccione algún evento", "Error",JOptionPane.ERROR_MESSAGE);
            return;
        }
        cod=Integer.parseInt(tf_codEvento.getText());
            resp=  JOptionPane.showConfirmDialog(null,"Esta seguro que desea eliminar?", "Confirmar Eliminación",JOptionPane.YES_NO_OPTION );
            if (resp==JOptionPane.YES_OPTION){
                entityManager.getTransaction().begin();
                Eventos e=entityManager.find(Eventos.class,cod);
                valor=e.toString();//guardamos el objeto antes de eliminar
                entityManager.remove(e);
                AuditoriaSistema as=new AuditoriaSistema();
                as.setAccion("Eliminación");
                as.setTabla("Evento");
                Date fecha=new Date();
                DateFormat formato=new SimpleDateFormat("dd-MM-yyyy HH:mm:ss");
                as.setFechaHora(formato.format(fecha));
                as.setUsuario("nadie");
                as.setAntes(valor);
                as.setDespues("No hay modificaciones");
                entityManager.persist(as);
                entityManager.getTransaction().commit();
                JOptionPane.showMessageDialog(null, "Eliminación Exitosa");
                list.remove(e);
                resetear();
            }else{
                     this.dispose();
            }
           
    }//GEN-LAST:event_btn_eliminarActionPerformed

