    private void inicializarBanco() {
        tf_identi.setText(""+entidad.getIdBanco());
        tf_nombre.setText(entidad.getNombre());
        asignarCb_tipoEntidad(entidad.getTipoEntidad());
    }
    private void asignarCb_tipoEntidad(String cadena){
        String tipo ;
        for(int i = 0; i < cb_tipoEntidad.getItemCount(); i++){
            tipo = cb_tipoEntidad.getItemAt(i).toString().toLowerCase();
            if(tipo.equals(cadena)){
                cb_tipoEntidad.setSelectedIndex(i);
                break;
            }
        }
    }

