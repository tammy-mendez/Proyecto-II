    private void btn_guardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_guardarActionPerformed
        // TODO add your handling code here:
        String antes;
        String despues;
         if (tf_codEmpleado.getText().length()==0){
            JOptionPane.showMessageDialog(null,"Seleccione un seguimiento", "Error",JOptionPane.ERROR_MESSAGE);
            return;
        }else{
              resp=  JOptionPane.showConfirmDialog(null,"Desea guardar los cambios?", "Confirmar Modificación",JOptionPane.YES_NO_OPTION );
             if (resp==JOptionPane.YES_OPTION){
                 EntityManager.getTransaction().begin();
                 lugarQuery=EntityManager.createNamedQuery("SeguimientoActividad.findByCodigoSeguimiento");
                 lugarQuery.setParameter("codigoSeguimiento", Integer.parseInt(tf_codigo.getText()));
                 List<SeguimientoActividad> seg=lugarQuery.getResultList();
                 antes=seg.get(0).toString();
                 SeguimientoActividad se=new SeguimientoActividad();
                 se.setCodigoSeguimiento(Integer.parseInt(tf_codigo.getText()));
                 se.setFechaHora(tf_fechaHora.getText());
                 Empleado e=obtenerEmpleado();
                 se.setCodigoEmpleado(e);
                 se.setLugar((Lugar) list_lugar.getSelectedItem());
                 se.setActividad((Actividad) list_descripcion.getSelectedItem());
                 EntityManager.merge(se);
                 EntityManager.flush();
                 despues=se.toString();
                 //registramos los datos necesarios para la auditoria
                 AuditoriaSistema as=new AuditoriaSistema();
                 as.setAccion("Modificación");
                 as.setTabla("Seguimiento de Actividad");
                 as.setAntes(antes);
                 as.setDespues(despues);
                 //trabajamos con la fecha
                 Date fecha=new Date();
                 DateFormat formato=new SimpleDateFormat("dd-MM-yyyy HH:mm:ss");
                 as.setFechaHora(formato.format(fecha));
                 as.setUsuario("nadie");
                 EntityManager.persist(as);
                 EntityManager.getTransaction().commit();
                 JOptionPane.showMessageDialog(null, "Modificación Exitosa");
                 List.remove(seg.get(0));
                 List.add(se);
                 resetear();
             }else{
                      this.dispose();
             }
         }
       
    }//GEN-LAST:event_btn_guardarActionPerformed

    private void tf_valorKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tf_valorKeyTyped
        // TODO add your handling code here:
        if (list_filtros.getSelectedItem()=="Código Seguimiento" || list_filtros.getSelectedItem()=="Código Empleado"){
            ch=evt.getKeyChar();
            if(!Character.isDigit(ch)){
                getToolkit().beep();
                evt.consume();
            }
        }
        else{
            if(list_filtros.getSelectedItem()=="Actividad" || list_filtros.getSelectedItem()=="Nombre" || list_filtros.getSelectedItem()=="Apellido"){
                ch=evt.getKeyChar();
                if(Character.isDigit(ch)){
                    getToolkit().beep();
                    evt.consume();
                }

            }
        }
    }//GEN-LAST:event_tf_valorKeyTyped

