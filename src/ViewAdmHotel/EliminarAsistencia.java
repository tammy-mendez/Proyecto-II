    private void btn_eliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_eliminarActionPerformed
        // TODO add your handling code here:
        int cod;
        String valor;
        if(tf_codEmpleado.getText().length()==0){
             JOptionPane.showMessageDialog(null,"Seleccione una asistencia", "Error",JOptionPane.ERROR_MESSAGE);
             return;
        }
        cod=Integer.parseInt(tf_codEmpleado.getText());
        query=entityManager.createNativeQuery("SELECT * FROM eventos WHERE "
                        + "codigoEmpleado= "
                        +cod+" AND fecha_inicio="+"'"+tf_fechaAsist.getText()+"'",Eventos.class);
                List<Eventos> e=query.getResultList();
                if(e.size()>=1){
                    JOptionPane.showMessageDialog(null, "Existen registros de eventos para esta asistencia,"
                    + "si elimina perderÃ¡ dichos registros","Aviso",JOptionPane.INFORMATION_MESSAGE );
                  
                }
        
            resp=  JOptionPane.showConfirmDialog(null,"Esta seguro que desea eliminar?", "Confirmar EliminaciÃ³n",JOptionPane.YES_NO_OPTION );
            if (resp==JOptionPane.YES_OPTION){
                entityManager.getTransaction().begin();
                if(e.size()>=1){
                    for(int i=0;i<e.size();i++){
                        valor=e.get(i).toString();
                        entityManager.remove(e.get(i));
                        registrarAuditoria("Eventos",valor);
                    }
                    entityManager.flush();
                }
                Asistencia a=entityManager.find(Asistencia.class,Integer.parseInt(tf_codAsistencia.getText()));
                valor=a.toString();//guardamos el objeto antes de eliminar
                entityManager.remove(a);
                entityManager.flush();
                registrarAuditoria("Asistencia",valor);
                entityManager.getTransaction().commit();
                JOptionPane.showMessageDialog(null, "EliminaciÃ³n Exitosa");
                list.remove(a);
                resetear();
            }else{
                     this.dispose();
            }
           
    }//GEN-LAST:event_btn_eliminarActionPerformed

